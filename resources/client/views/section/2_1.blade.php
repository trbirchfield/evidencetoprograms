                <h2>Sources of secondary data</h2>
                <p>Secondary data are data that have already been collected by someone else. Secondary data are advantageous because they are often available for immediate use and are inexpensive, if not free, to use. However, because secondary data were collected previously, it is difficult to determine if the data are still applicable to the audience or if appropriate methodologies were used during data collection. These are significant disadvantages. Obtaining secondary data from reputable sources, several of which are identified below, can help you capitalize on the advantages of secondary data and guard against the disadvantages.</p>
                <ul class="document-list">
                    <li>
                        <em>Government agencies</em>
                        <ul>
                            <li>
                                <em>International</em> &mdash; Abundant data on global health can be found at the <a href="http://www.who.int/en/" target="_blank">World Health Organization (WHO) website </a> and the websites of its regional offices (European, South-East Asia, Americas, etc.). The WHO is the directing and coordinating authority for health within the United Nations system. Data and statistics are available by health topic or by nation. Additionally, the WHO website contains a number of publications on evidence-based practices and programs.
                            </li>
                            <li>
                                <em>National</em> &mdash; Numerous governmental organizations provide useful information for program planners. Demographic data (e.g., median household income, percent of population over age 65) can be found on the <a href="http://www.census.gov/" target="_blank">U. S. Census Bureau website</a>. The Centers for Disease Control and Prevention (CDC), a component of the U. S. Department of Health and Human Services, provides health data in a variety of formats <span class="reference">(see Table 3)</span> on <a href="http://www.cdc.gov" target="_blank">its website</a>.
                            </li>
                            <li>
                                <em>State and local</em> &mdash; Each state has a respective state-level health agency and local (e.g., regional, county, city) health agencies. Various terms are used to denote these agencies, including health departments, health services, and health districts. These agencies often conduct periodic needs assessments, which may contain important information regarding your audience. They may also provide information regarding funding opportunities for EBPs. In Texas, the <a href="http://www.dshs.state.tx.us/default.shtm" target="_blank">Department of State Health Services</a> is the state-level health agency. Useful information available on the Department of State Health Services website includes disease and death rates for the state, details on available health services, and contact information for local health departments.
                            </li>
                        </ul>
                    </li>
                    <li>
                        <em>Non-governmental agencies and organizations</em>
                        <ul>
                            <li>
                                <em>Voluntary health organizations</em> &mdash; Voluntary health agencies are nonprofit organizations comprised of individuals (lay and professional) who seek to improve and protect the health of a particular group of people or combat a particular disease, disability, or type of disease or disability. These agencies receive funding from voluntary contributions. Their activities often include education, advocacy, research funding, patient support groups, and other services to affected individuals and communities. As part of their educational efforts, voluntary health organizations will often provide data related to their respective disease or disability emphases. Well-known voluntary health organizations include the <a href="http://www.cancer.org/" target="_blank">American Cancer Society</a> and the <a href="http://www.heart.org/HEARTORG/" target="_blank">American Heart Association</a>. There are voluntary health organizations for almost all diseases and disabilities. Speaking with individuals with these conditions, contacting local healthcare specialists, and searching the internet can help you identify these organizations.
                            </li>
                            <li>
                                <em>Foundations</em> &mdash; Organizations established by endowment with provision for future maintenance are considered to be foundations. Several health-related foundations provide useful data for determining the needs of an audience. For example, the Robert Wood Johnson Foundation, the nation's largest philanthropy devoted solely to the public's health, partners with the University of Wisconsin Population Health Institute and Community Catalyst to maintain the <a href="http://www.countyhealthrankings.org/#app/" target="_blank">County Health Rankings &#38; Roadmaps program</a> . The Rankings portion of the program provides information on death and disease rates, as well as data on health behaviors, healthcare access, and socioeconomic factors for each county in the U. S. The Roadmaps portion of the program provides grants to local coalitions and partnerships among the policy, business, education, public health, healthcare, and community sectors; grants to national organizations working to improve health; recognition of communities whose promising efforts have led to better health; and customized technical assistance on strategies to improve health. Other foundations that provide useful data include the <a href="http://www.kff.org/" target="_blank">Henry J. Kaiser Family Foundation</a>, which focuses on healthcare, statistics, and the <a href="http://www.jhartfound.org/" target="_blank">John A. Hartford Foundation</a>, which focuses on improving healthcare for older Americans.
                            </li>
                            <li>
                                <em>Academic institutions</em> &mdash; Many universities and colleges have needs assessment data on the communities surrounding their institutions. For example, the Center for Community Health Development of the Texas A&#38;M Health Science Center School of Rural Public Health, in conjunction with the Brazos Valley Health Partnership, generated the <a href="/public/content/BV_Health_Assessment_ExecReport_2010.pdf" target="_blank">Brazos Valley Health Assessment</a>, a comprehensive, valid, and reliable source of needs assessment information for the Brazos Valley region of Texas. Another example is The Lower Rio Grande Valley Community Health Assessment generated by The University of Texas Health Science Center at Houston School of Public Health. Contact local institutions to determine if they have any relevant needs assessment data. Colleges and universities can also be a source for interns, technical assistance, and funding for EBPs.
                            </li>
                            <li>
                                <em>Healthcare organizations</em> &mdash; Hospitals and other healthcare institutions often collect needs assessment data on the communities in their service areas. Though any healthcare organization may provide this sort of data, it is especially beneficial to check with non-profit hospitals, which are now required by provisions in the Patient Protection and Affordable Care Act to conduct community health needs assessments and then implement plans to meet identified needs. An example resource is the community health needs assessment <a href="http://www.sw.org/community-benefit/community-health-needs-assessment" target="_blank">conducted by Scott &amp; White Healthcare</a>, a non-profit hospital system in Central Texas. On this page, users can find information on demographics, community health indicators, health disparities, and promising practices.
                            </li>
                        </ul>
                    </li>
                </ul>
