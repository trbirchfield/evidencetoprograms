                <h2>Sources of Primary Data</h2>
                <p>Primary data are data you collect yourself. Generally, primary data answer questions that are tailored to your audience and situation, which is a major advantage of this data type. A major disadvantage of primary data is the need to assure the validity and reliability of the tools and techniques used to collect the data. Validity is the degree to which a tool (e.g., survey) or technique (e.g., observation) measures what it is designed to measure. Reliability is the degree to which a tool or technique yields results that are consistent. While the abovementioned definitions are succinct, there are several types of validity and reliability. Experts with backgrounds in research methods and statistics are best able to determine which tools and techniques are sound. If you do not feel anyone at your organization is well suited to determine if tools and techniques are valid and reliable, you might benefit from existing instruments that can be adapted and used by your organization <span class="reference">(see Figure 3)</span>. The advantages and disadvantages of various primary data collection methods are summarized in Table 2.</p>
                <h2>Single-step or cross-sectional surveys</h2>
                <p>In single-step or cross-sectional surveys, participants are asked to self-report answers to questions during a single contact. Surveys can be used to gather information about a variety of topics including subjective experiences (e.g., health concerns, health behaviors, feelings about a health program) and preferences (e.g., health programs of interest, the best location or time to offer a program). Though self-reported measures can evaluate a number of topics, they rely on participants’ abilities to accurately recall information and to answer honestly. Several survey approaches are discussed below.</p>
                <ul class="document-list">
                    <li>
                        <em>Written questionnaires</em> &mdash; Surveys in the form of a written questionnaire are frequently used to collect self-reported data. Written questionnaires can be distributed to participants in a number of ways, including mail. It is best to use simple and clear language on a questionnaire to avoid confusion. It is also beneficial to place questions in a logical order. Seniors often have trouble with their vision, so it is best to use a 14 point (or larger) font. To encourage completion, surveys should be concise and to-the-point. A mix of open-ended questions, which prompt narrative answers (e.g., What sports do you play?), and close-ended questions, which offer response choices (e.g., Please place a check mark by each sport you play: __Football __Basketball __ Soccer __Tennis), can be used. Remember that open-ended questions generally take longer to answer and require more effort than close-ended questions. Personal or sensitive questions (e.g., income, ethnicity, drug or alcohol use) should be limited to the extent possible and placed towards the end of the survey. Provide clear instructions, keep the survey pages uncluttered, use an easy-to-read font, and provide plenty of room for responses. Comprehensive information on questionnaire design can be found in Module 4 &mdash; <a href="http://www.cdc.gov/reproductivehealth/ProductsPubs/PDFs/Epi_Module_04_Tag508.pdf" target="_blank"> Questionnaire Design of the Centers for Disease Control and Prevention’s Reproductive Health Epidemiology Series</a>
                        <ul>
                            <li>
                                <em>Electronic questionnaires</em> &mdash; Electronic questionnaires are written questionnaires that are in an electronic format. They may be distributed by email or linked to webpages. These questionnaires may be the most affordable and quickest to administer. Special attention must be paid to privacy when using electronic questionnaires. Additionally, some seniors may be uncomfortable with computer technology, so be sure to assess your audience to see if they are at ease with an electronic format.
                            </li>
                        </ul>
                    </li>
                    <li>
                        <em>Telephone interviews</em> &mdash; Trained interviewers, along with a questionnaire that includes a script, are required to conduct telephone interviews. The interviewers must be careful to ask the questions in an objective manner, so they do not &ldquo;lead&rdquo; participants to answer in a given way. The organization conducting the interviews must have access to a directory of audience members’ phone numbers. The Wallace Foundation has created a <a href="http://www.wallacefoundation.org/knowledge-center/after-school/collecting-and-using-data/Documents/Workbook-F-Telephone-Surveys.pdf" target="_blank">useful guide for organizations interested in conducting telephone surveys.</a>
                    </li>
                    <li>
                        <em>Face-to-face interviews</em> &mdash; Questionnaires can also be administered through face-to-face interviews. Trained interviewers who can build rapport with participants are needed for this approach. Like in telephone interviews, the interviewers must be careful to ask the questions in an objective manner, so participants are not led to answer in a particular way. This is generally the most expensive and most time consuming method of questionnaire administration. Additional information about face-to-face interviews is available in the previously mentioned questionnaire design document produced by the Centers for Disease Control and Prevention (see section on written questionnaires).
                    </li>
                </ul>
                <h2>Delphi Technique</h2>
                <p>The Delphi technique is a form of multi-step survey where the administrators contact participants more than once. Generally, participants are contacted via mail or email with one or two broad questions. The responses to these questions are analyzed and a second questionnaire, with more specific questions, is returned to participants. Several iterations or cycles of this process (usually five or fewer) will occur until consensus is reached. The Delphi technique is often used with groups of experts on a given topic. During a needs assessment, key informants (individuals with the knowledge and ability to report the needs of the audience) serve as experts. Additional details on the Delphi method, including sample invitation letters, <a href="http://www.uwex.edu/ces/pdande/resources/pdf/Tipsheet4.pdf" target="_blank">are available on a tip sheet</a> developed by the University of Wisconsin Cooperative Extension.</a></p>
                <h2>Community Forums</h2>
                <p>Community forums, also termed town hall meetings, bring together the audience and needs assessors. The forums or meetings are often held in easily accessible buildings, such as libraries or schools, and are scheduled at convenient times. Once participants have gathered, a moderator will explain the meeting purpose and will ask participants to share their ideas and concerns. Generally, participants will stand up before the group or even stand before a centrally located microphone when sharing. The moderator should be skilled in maintaining order, keeping the meeting on track, and eliciting responses from participants who are reluctant to share. Recorders should take notes or audio record the session so responses can be analyzed after the meeting. Tips on planning a town hall meeting are available from the <a href="http://www.cdc.gov/women/events/townhall/index.htm" target="_blank">Centers for Disease Control and Prevention</a>.</p>
                <h2>Focus Groups</h2>
                <p>A group of 8-12 individuals are guided through an open discussion during a focus group. Ideally, focus groups are comprised of similar individuals who do not know one another. Both the similarity among individuals and the fact that they will not see one another again lower inhibitions and encourage sharing. The moderator stimulates free-flowing conversation by providing approximately 10 open-ended questions as prompts. While free-flowing conversation is the goal, the moderator helps participants focus on the questions at hand in order for needs assessors to obtain the information they need. A 45-90 minute discussion is long enough to gather data without fatiguing participants. It is best for an audio recording to be made so the conversation can be transcribed and analyzed for themes. Always be sure to ask permission from participants before recording them. Also, it is beneficial for an observer to take notes during the focus group in case the recording fails. <a href="http://ctb.ku.edu/en/tablecontents/sub_section_main_1018.aspx" target="_blank">The Community Tool Box of the University of Kansas</a> provides guidelines for planning and conducting focus groups: </p>
                <h2>Nominal Groups</h2>
                <p>Nominal groups are comprised of approximately five to ten participants. A facilitator will present the participants with a question. Each participant then documents his or her answer silently and independently. Once each participant has documented an answer, each shares his or her response in a round-robin fashion. The facilitator documents each answer in a way that all participants can see (e.g., chalk board, white board, flipchart). Once all of the responses have been documented, the participants discuss the responses for clarification and then rank-order the responses by importance. In some cases, participants vote privately to rank-order the responses. In other cases, participants will have a preliminary vote that is followed by discussion before conducting a final vote. <a href="http://www.cdc.gov/healthyyouth/evaluation/pdf/brief7.pdf" target="_blank">A brief on nominal groups is available from the Centers for Disease Control and Prevention</a></p>
                <h2>Observation</h2>
                <p>Members of the audience and the environment surrounding them can be observed as part of a needs assessment. For example, needs assessors may stand on a street corner and observe drivers to determine how many are wearing seatbelts. Additional behaviors and environmental conditions that can be observed include the use of hard hats by workers in a construction site, cigarette smoking by employees during work breaks, or vegetable consumption by individuals in a cafeteria. A popular form of observation is a windshield survey or walking tour, during which assessors drive and/or walk through a designated area, viewing as much of it as possible. While moving through the area, they watch for various predetermined elements (e.g., public transportation availability, housing type and quality, existence and quality of parks). The assessors document notes about what they see, hear, and smell regarding the elements. The notes are later analyzed for themes. <a href="http://ctb.ku.edu/en/tablecontents/chapter3-section21-main.aspx" target="_blank">A brief guide to windshield surveys is available from The Community Tool Box of the University of Kansas</a>.</p>
