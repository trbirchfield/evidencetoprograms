            <div class="small-9 columns doc-right">
                <p>The first step in selecting an EBP is to identify the needs of your audience. Knowing the needs of your audience will enable you to select an intervention that is relevant and has the potential to make a positive impact. &ldquo;Needs&rdquo; can be defined simply as the difference between present conditions and more desirable conditions. Needs are generally classified into two categories: actual (or true) and perceived (or reported). Actual needs can be objectively identified and are substantiated or demonstrated by measurements, such as death or disease rates. Perceived needs are reported by audience members as they draw from their experiences, ideas, feelings, and instincts.</p>
                <p>Actual and perceived needs are not mutually exclusive. Often times the two will overlap because audience members will perceive needs that are substantiated by objective data. It would take enormous amounts of time and energy to classify all of an audience’s needs as perceived, actual, or both. A more efficient approach is to identify as many needs as possible and then determine which of these can be addressed through your organization’s programming.</p>
                <p>Needs can be identified through primary and secondary sources of data. These data types are explained in the subsequent sections. Though it is ideal to gather data from numerous sources, your organization may not have the resources to use all of the sources that are provided as examples. Do not let this discourage you from assessing your audience’s needs. Simply select the sources that are realistic given your organization’s resources. If at all possible, use at least one primary and one secondary source. For information on what to do with needs assessment data once it is collected, see the content on <a href="/section/2/2">Prioritizing Needs.</a></p>
