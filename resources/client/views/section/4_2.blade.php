                    <h5>Additional Resource</h5>
                    <p><a href="http://whatworks.uwex.edu/attachment/whatworks_04.pdf" target="_blank">Program Fidelity and Adaptation: Meeting Local Needs without Compromising Program Effectiveness</a>, developed by the Cooperative Extension of the University of Wisconsin-Madison, provides guidance for adapting programs while maintaining fidelity.</p>
