                <p>What if your organization has committed to implementing a given program and you realize that fidelity may be threatened because you need to adapt several key elements to make the program suitable for your audience? You are not necessarily trapped; there are a few courses of action to consider. First, get in touch with the program developers. They will be able to provide insight into the impact the adaptations will have on outcomes. Additionally, the developers may be interested in working with you and collecting data to evaluate the impacts of the adaptations on program outcomes, as this would contribute to the program’s evidence base. Second, find additional resources. Adaptations are often necessary because of resource limitations (e.g., limited funding, insufficient staff, inadequate facilities). If this is the case for you, then reevaluate your funding options and your opportunities for partnerships with organizations that have the resources you lack <span class="reference">(see <a href="/section/2/9">Obtaining Funding</a> and <a href="/section/2/8">Establishing Partnerships</a> for more information)</span>. Finally, you can select an alternative program. While this is a difficult choice if you have already invested time and money into a program, it is important to consider the consequences of moving forward with a program that will be implemented without fidelity due to extensive adaptations.</p>
