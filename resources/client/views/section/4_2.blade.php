            <div class="small-9 columns doc-right search-index">
                <p>Before determining which changes can be made to a given program without endangering fidelity, it is wise to reconsider your selection. Adapting the program could ultimately require more resources than would be required to implement the program as originally designed. How well does the program meet your organizational needs and the needs of your audience? Will it appeal to your audience? Does it target relevant outcomes? If an alternative program that requires little or no adaptation is available, now is the time to consider changing courses and implementing it instead.</p>
                <p>It is a good idea to contact the developer of the program to discuss proposed adaptations. The program developer will be able to tell you which adaptations have been made before. He or she will also be able to elaborate on the theories and assumptions that influenced the program’s development. Getting input from the developer is the best way to ensure that any adaptations made to the program are appropriate. The contact information for program developers can be found on databases of EBPs, including those discussed under <a href="/section/2/3">Identifying Evidence-Based Interventions</a>.</p>
                <p>In general, adaptations can be made to two aspects of a program: key elements (or components) and characteristics. Key elements or components are the &ldquo;active ingredients&rdquo; of a program—the components that generate the positive outcomes. Characteristics of a program are the appearances and features that distinguish a program. Characteristics can usually be adapted without influencing program outcomes. However, great caution should be exercised when adapting key elements. As noted earlier, the program developers are best suited to make adaptations to key elements. Table 4 provides examples of program adaptations and indicates the degree of caution that must be exercised when making each adaptation. Keep in mind that though characteristics can often be adapted without changing outcomes, you may still need permission from the program developer to make changes in order to adhere to copyright laws.</p>
