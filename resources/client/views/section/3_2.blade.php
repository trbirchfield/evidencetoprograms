                <p>Impact evaluation assesses the immediate, observable effects of a program. Examples include changes in the knowledge, attitudes, skills, or behaviors of program participants. Outcome evaluation assesses the long-term effects of a program on the health status of participants. Examples include changes in disease and death rates. Impact and outcome evaluations are often referred to as summative evaluation, which is evaluation used to draw conclusions about the impacts, outcomes, and benefits of a program.</p>
                <p>Innumerable program effects can be assessed through impact and outcome evaluation. These will not be outlined here because they are often specific to the health topic addressed by a given intervention. However, one concept that is universally applicable to impact and outcome evaluation is discussed in the subsequent paragraph.</p>
                <p>It is critical to collect baseline data (information about participants that is collected before they participate in a program). Baseline data is often collected through pretesting (the administration of instruments to participants before beginning a program). Pretest data can be compared to posttest data (the administration of the same instruments to the same participants after theyâ€™ve completed a program) in an attempt to identify changes and attribute the changes to specific program components. The instruments administered to participants during pre and posttests often include anthropometric and physiologic measures (e.g., height, weight, blood pressure, heart rate), as well as surveys and questionnaires that assess behaviors, knowledge, and constructs (e.g., stress, intelligence, self-efficacy).</p>
