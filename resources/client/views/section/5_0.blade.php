                <p>Before members of your target audience can be reached with your program, they must first know about the program and be interested in it. Awareness and interest are generated through marketing and participant recruitment. Marketing is a process for generating, communicating, and distributing value to consumers. In commercial marketing, the value is generally a product or service that is provided in exchange for a financial payment. In social marketing, the value is generally a behavior, service, or event. Social marketing is concerned with behavior change for improved health, not financial gain. Social marketing combined with specific actions to decrease barriers to participation are the means by which you can recruit members of your target audience.</p>
                <p>There are several questions to ask as you begin developing your campaign.</p>
                <ul class="document-list">
                    <li><em>When should I begin promoting the program?</em> In general, the earlier you can get started, the better. A rule of thumb is to begin marketing your program between eight weeks and four weeks before it begins.</li>
                    <li><em>Who is my audience and what do they care about?</em> Reaching your audience effectively requires knowledge of where they live, where they congregate, their interests, and their sources of information.</li>
                    <li><em>Which marketing methods will reach my audience?</em> Based on the information learned about your audience from the above questions, select suitable marketing approaches. Consider face-to-face meetings if your audience tends to congregate in certain locations, such as senior centers or a church. Hanging posters in these locations and distributing flyers may also be beneficial. If your audience is computer savvy, consider using email or social media. If they are not, then direct mail might be a better option. Enlist the help of program champions to promote the program with word-of-mouth marketing.</li>
                    <li><em>How much can I spend on marketing?</em> Consider your budget and the methods you would like to use to promote your program. Select the methods you can afford, remembering that some mediums (e.g., TV commercials) will cost more than others (e.g., posters hung on community bulletin boards).</li>
                    <li><em>What message do you want to communicate?</em> Be sure to choose your words carefully. For example, are you implementing a &ldquo;health promotion&rdquo; or a &ldquo;disease prevention&rdquo; program? Is your program for people with &ldquo;ongoing health conditions&rdquo; or for &ldquo;chronically ill&rdquo; people? Does your program &ldquo;help you connect with others&rdquo; or does it &ldquo;prevent feelings of loneliness&rdquo;? How you phrase your marketing messages can make a huge difference in how the program is perceived.</li>
                    <li><em>How can I network and build infrastructure to support ongoing marketing?</em> What relationships do you have with other organizations that can help you spread the word about your program? Can you leverage connections you have with radio stations or local newspapers? Make plans, if not already in place, to keep in touch with the individuals who do participate in your program so you can contact them in the future about new programs and opportunities. Sending periodic newsletters and hosting social events are examples of how you can maintain contact with former participants.</li>
                </ul>
                <h2>Social marketing and the marketing mix</h2>
                <p>Social marketing is the application of commercial marketing techniques to campaigns designed to influence the behavior of individuals to improve their welfare and that of society as a whole. The most prevalent technique in commercial marketing is the management of the marketing mix components, which are sometimes referred to as the &ldquo;Four Ps&rdquo; <span class="reference">(see Figure 6)</span>. Likewise, program implementers can manipulate the Four Ps in social marketing to make sure they promote the right product in the right place for the right price.</p>
                <p>Strategies related to each of the Four Ps can be employed to increase the likelihood that the marketing mix will accomplish its purpose. Several of these strategies are listed below.</p>
                <ul class="document-list">
                    <li><em>Product</em>
                        <ul>
                            <li>Involve members of the target audience in the selection of the program.</li>
                            <li>Use needs assessment data to guide program selection so the selected program is relevant to the audience.</li>
                            <li>Translate program materials into the native language of your audience.</li>
                            <li>Utilize lay leaders and staff members who reflect the characteristics (e.g., ethnicity, age, socioeconomic status) of your audience.</li>
                            <li>Link the program to a trusted individual or institution in the community.</li>
                            <li>Help audience members see the program as beneficial (for example, use the term &ldquo;health promotion&rdquo; as opposed to &ldquo;disease prevention&rdquo;).</li>
                        </ul>
                    </li>
                    <li><em>Price</em>
                        <ul>
                            <li>Offer programs for free.</li>
                            <li>If you do charge for a program, offer scholarships and reduce fees on a sliding scale.</li>
                            <li>Emphasize the benefits of the program (i.e., the &ldquo;return on investment&rdquo;).</li>
                        </ul>
                    </li>
                    <li><em>Place</em>
                        <ul>
                            <li>Offer the program in facilities that are easy to locate and access.</li>
                            <li>Offer the program at multiple locations, especially at those where your audience typically congregates.</li>
                            <li>Offer the program at convenient times.</li>
                        </ul>
                    </li>
                    <li><em>Promotion</em>
                        <ul>
                            <li>Utilize multiple advertising approaches (e.g., mass media, flyers and posters, direct mail).</li>
                            <li>Conduct social events in venues frequented by your audience.</li>
                            <li>Offer previews or trials of the program.</li>
                            <li>Consider using social media, which is becoming popular among seniors.</li>
                            <li>Encourage participants to invite family and friends to take part in the program.</li>
                        </ul>
                    </li>
                </ul>
