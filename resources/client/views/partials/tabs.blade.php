					<h1>Toolkit Glossary</h1>
					<p id="got-info">scroll to see all</p>
					<div class="glossary">
						<div class="glossary-left">
							<p><em>Adaptation</em>– Making changes to an evidence-based program in order to make it more suitable for a particular organization and/or audience.</p>
							<p><em>Baseline</em> – A starting point. In evidence-based programming, the term “baseline” is usually used in the context of data collection, where baseline data is data collected before a program is implemented.</p>
							<p><em>Credentials</em> – A testimony of qualification, competence, or authority issued to an individual by a third party. Examples of credentials include academic diplomas, academic degrees (e.g., MSW, MPH, PhD), licenses (e.g., MD, RN, LCSW), certifications (e.g., CHES, CPR, first aid), and security clearances.</p>
							<p><em>Evidence </em>– Facts or testimony in support of a conclusion, statement, or belief. In some settings, individuals may refer to “levels of evidence” or “types of evidence.” These terms will have specific definitions unique to the setting in which they are used. When referring to evidence-based programs, the term “evidence” is generally used to describe the findings or results of program evaluation studies.</p>
							<p><em>Evidence-based program</em> – A program that has been thoroughly evaluated by researchers who determined it produces positive outcomes.</p>
							<p><em>Fidelity</em> – The extent to which a program is being implemented as its developers intended for it to be.</p>
							<p><em>Implementation</em> – Putting into action or carrying out a program.</p>
							<p><em>Instrument</em> – A measurement tool. Instruments can take many forms including biomedical equipment (e.g., glucometer, blood pressure monitor, weight scale), pencil and paper tests, questionnaires, and interviews. A thermometer is an instrument used to measure body temperature. Likewise, a survey is an instrument that can be used to measure anxiety.</p>
							<p><em>Medicaid</em> – A publically-funded health insurance program for individuals who have low incomes and fall into certain categories of eligibility.</p>
							<p><em>Objectives</em> – Specific, measurable steps that can be taken to achieve goals.</p>
							<p><em>Peer review</em> – When experts review a professional’s performance, research, or writings. Peer review is a way that qualified professionals self-regulate their professions. Performance, research, or writings that pass the peer review process have increased credibility or trustworthiness.</p>
							<p><em>Program champion</em> – An individual who advocates for a program.</p>
							<p><em>Quality assurance</em>- A collection of planned, systematic activities applied to ensure that the components of a program are being implemented well.</p>
							<p><em>Secondary data</em> – Previously collected data that is being used for a purpose other than that for which it was originally collected.</p>
							<p><em>Theory of behavior change</em> – An attempt to explain how and why people change their behaviors. Researchers typically generate theories of behavior change from research in the fields of psychology, education, health, and other social sciences. When developing evidence-based programs, researchers will select a theory or components from several theories to guide program development.</p>
						</div>
						<div class="glossary-right">
							<p><em>Audience</em> – The individuals for whom you implement your program. Depending on your setting, these individuals may also be referred to as a target population, population of interest, or clientele.</p>
							<p><em>Buy-in</em> – Typically used in the business world, buy-in refers to a financial exchange. In the context of health programs, the buy-in of stakeholders (community members, organizational leaders, participants, etc.) is generally non-financial. It involves their acceptance of a concept, idea, or proposal.</p>
							<p><em>Data</em> – A collection of facts, such as measurements and statistics.</p>
							<p><em>Evidence-based practice</em> – When clinicians (e.g., doctors, nurses) base their healthcare treatment decisions on the findings of current research, their clinical expertise, and the values/preferences of their patients.</p>
							<p><em>Evidence-informed practice or program</em> – A practice or program that is guided by theories and preliminary research. While there is some indication that these practices and programs produce positive outcomes, the evidence is too weak to refer to them as evidence-based. These are sometimes referred to as “promising” or “emerging” practices and programs.</p>
							<p><em>Goals</em> – General, non-measurable intentions or outcomes.</p>
							<p><em>Incentives for participation</em> – Factors that motivate an individual to take part in a program. Organizations sometimes provide incentives to encourage participants to begin and/or remain enrolled in a program. Common incentives include gift cards and program t-shirts.</p>
							<p><em>Intervention</em> – Organized efforts to promote health and prevent disease. This term is used because the efforts intervene, or come between, an individual and a negative health outcome in an attempt to prevent or delay the negative outcome. “Intervention” and “program” are often used interchangeably.</p>
							<p><em>Interventionist</em> – An individual who implements or carries out the components of a program.</p>
							<p><em>Lay leaders</em> – Individuals who do not have formal healthcare credentials who are trained to lead evidence-based programs.</p>
							<p><em>Medicare</em> – A publically-funded health insurance program for adults over age 65 and individuals with certain disabilities or health conditions.</p>
							<p><em>Partnership</em> – A cooperative relationship between two or more organizations that collaborate to achieve a common goal through the effective use of knowledge, personnel, and other resources.</p>
							<p><em>Primary data</em> – Original or new data being collected for a specific research goal.</p>
							<p><em>Protocols</em> - Predefined procedural methods. Examples include detailed program implementation procedures, required equipment, required data collection instruments with detailed instructions for administration, and recommended safety precautions.</p>
							<p><em>Readiness</em> – The degree to which an organization is prepared or ready for something.</p>
							<p><em>Stakeholder</em> – Any individual or group that has a stake or interest in a program.</p>
							<p><em>Translation</em> – The process of taking a program originally implemented in a controlled, laboratory-like setting and making it suitable for implementation in the community.</p>
						</div>
